package java.util;

// NeOak stdlib stub for IDEs. Implementation is provided by the runtime shim (neoak.rt.Scanner).
public class Scanner {
    public Scanner(Object source) {}
    public boolean hasNext() { return false; }
    public String next() { return null; }
    public String nextLine() { return null; }
    public int nextInt() { return 0; }
    public long nextLong() { return 0; }
    public double nextDouble() { return 0.0; }
    public boolean nextBoolean() { return false; }
    public Scanner useDelimiter(String pattern) { return this; }
    public void close() {}
}

